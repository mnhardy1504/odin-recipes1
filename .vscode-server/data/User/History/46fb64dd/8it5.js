const multiply = function(a, b) {    // function to multiply tw numbers
    return a * b; 
}

const add = function(a, b) {
    return a + b;
}

const subtract = function(a, b) {
  return a - b;
}

const divide = function(a, b) {
   if (b === 0) {
    return "Cannot divide by zero";
   } else {
     return a / b; 
   }
};


